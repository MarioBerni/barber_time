/// Clase que centraliza todos los espaciados estándar de la aplicación.
///
/// Proporciona constantes para márgenes, paddings y separaciones.
class AppSpacing {
  // Espaciados básicos
  /// Espaciado nulo (0.0).
  static const double zero = 0.0;

  /// Espaciado mínimo (2.0).
  static const double xxxs = 2.0;

  /// Espaciado extra extra pequeño (4.0).
  static const double xxs = 4.0;

  /// Espaciado extra pequeño (8.0).
  static const double xs = 8.0;

  /// Espaciado pequeño (12.0).
  static const double sm = 12.0;

  /// Espaciado mediano, el más común (16.0).
  static const double md = 16.0;

  /// Espaciado grande (20.0).
  static const double lg = 20.0;

  /// Espaciado extra grande (24.0).
  static const double xl = 24.0;

  /// Espaciado muy grande (32.0).
  static const double xxl = 32.0;

  /// Espaciado enorme (40.0).
  static const double xxxl = 40.0;

  /// Espaciado gigante (48.0).
  static const double huge = 48.0;

  /// Espaciado muy gigante (56.0).
  static const double giant = 56.0;

  /// Espaciado extremo (64.0).
  static const double extreme = 64.0;

  // Espaciados específicos
  /// Espaciado estándar entre elementos de un formulario.
  static const double formElementSpacing = md;

  /// Espaciado vertical entre secciones de una página.
  static const double sectionSpacing = xxl;

  /// Relleno interno estándar para tarjetas (cards).
  static const double cardPadding = lg;

  /// Relleno horizontal estándar para las páginas.
  static const double pagePadding = md;

  /// Espaciado estándar entre elementos de una lista.
  static const double listItemSpacing = sm;

  // Espaciado para componentes específicos
  /// Relleno vertical para botones.
  static const double buttonPaddingVertical = sm;

  /// Relleno horizontal para botones.
  static const double buttonPaddingHorizontal = xl;

  /// Relleno interno para campos de texto.
  static const double inputFieldPadding = md;

  /// Altura estándar de la barra de aplicación (AppBar).
  static const double appBarHeight = 56.0;

  /// Altura estándar de la barra de navegación inferior.
  static const double bottomNavHeight = 64.0;

  /// Relleno para el botón de acción flotante.
  static const double floatingActionButtonPadding = md;

  // Insets (para uso con EdgeInsets)
  /// Inset nulo (0.0) para EdgeInsets.
  static const insetZero = 0.0;

  /// Inset extra extra pequeño (4.0) para EdgeInsets.
  static const insetXXS = 4.0;

  /// Inset extra pequeño (8.0) para EdgeInsets.
  static const insetXS = 8.0;

  /// Inset pequeño (12.0) para EdgeInsets.
  static const insetSM = 12.0;

  /// Inset mediano (16.0) para EdgeInsets.
  static const insetMD = 16.0;

  /// Inset grande (20.0) para EdgeInsets.
  static const insetLG = 20.0;

  /// Inset extra grande (24.0) para EdgeInsets.
  static const insetXL = 24.0;

  /// Inset muy grande (32.0) para EdgeInsets.
  static const insetXXL = 32.0;
}
